
public class Logico {

	public static void main(String[] args) {
		
		/* Operadores Logicos
		 * E: 				&&
		 * OU:				||
		 * OU Exclusivo: 	^
		 * NÃ£o: 			!
		 * */
		
		boolean a = true;
		boolean b = false;
		
		System.out.println(a && b);
		System.out.println(a || b);
		System.out.println(a ^ b);
		System.out.println(!b);
	}

}